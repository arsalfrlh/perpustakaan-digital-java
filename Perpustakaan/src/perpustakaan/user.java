    private ArrayList<String> id_list = new ArrayList();
    private void tampil(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No");
        model.addColumn("Nama");
        model.addColumn("Username");
        txt_id.setVisible(false);
        
        try{
            String query = "SELECT * FROM tbl_user";
            java.sql.Connection koneksi = Perpustakaan.config();
            java.sql.Statement sql = koneksi.createStatement();
            java.sql.ResultSet hasil = sql.executeQuery(query);
            
            int no = 1;
            id_list.clear();
            while(hasil.next()){
                id_list.add(hasil.getString("id_user"));
                model.addRow(new Object[]{no++,hasil.getString("nama"),hasil.getString("username")});
            }
            tabel.setModel(model);
        }catch(Exception e){
            
        }
    }

